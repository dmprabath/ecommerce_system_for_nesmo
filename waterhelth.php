		</div>
		<div class="col-lg-12 col-sm-12">
			<p>Reverse osmosis is a continuously operating treatment technology that uses pressure to pass source water through a thin membrane and thereby separate impurities from water.</p>

<p>RO works by reversing the principle of osmosis, the natural tendency of water with dissolved salts to flow through a membrane from lower to higher salt concentration. This process is found throughout nature. Plants use it to absorb water and nutrients from the soil. In humans and other animals, kidneys use osmosis to absorb water from blood.</p>

<p>In an RO system, pressure (usually from a pump) is used to overcome natural osmotic pressure, forcing feedwater with its load of dissolved salts and other impurities through a highly sophisticated semipermeable membrane that removes a high percentage of the impurities. The product of this process is highly purified water.</p>

<p>The rejected salts and impurities concentrate above the membrane and are passed from the system to drain or onto other processes. In a typical commercial industrial application, 75% of the feedwater is purified. In applications in which water conservation is important, 85% of the feedwater is purified.</p>

<p>An RO system uses cross-filtration, where the solution crosses the filter with two outlets: the filtered water goes one way and the contaminated water goes another way. To avoid buildup of contaminants, cross-flow filtration allows water to sweep away contaminant buildup and enough turbulence to keep the membrane surface clean. </p>
